void InitializingStack(Stack* tStack) {

	tStack->size = 0;

}



void PushingStack(Stack* tStack, int num) {

	tStack->size++;
	if (0 < tStack->size) { // stack의 용량 범위 이내라면 push를 진행함.
		Node* node = (Node*)malloc(sizeof(node));
		node->data = num;
		node->next = NULL;

		tStack->top = node;
	}

	printf("data value : %d\n\n", tStack->top->data);
}